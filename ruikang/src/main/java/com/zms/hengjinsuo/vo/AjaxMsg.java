package com.zms.hengjinsuo.vo;


//封装 给ajax 提交的请求的返回信息
public class AjaxMsg {
	
	private int code;
	
	private String msg;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
